//! clap App for command cli
use clap::{Command, Arg};

pub const VERSION: &str = "0.2.0";

pub fn build_app() -> Command<'static> {
    let run_command = Command::new("run")
        .about("Run scripts from catalog")
        .arg(
            Arg::new("script")
                .required(true)
                .help("script name")
                .index(1)
        ).arg(
        Arg::new("params")
            .required(false)
            .help("params")
            .index(2)
            .multiple_values(true)
    );
    let open_command = Command::new("open")
        .about("Open catalog on GitHub")
        .arg(
            Arg::new("script")
                .required(true)
                .help("script name")
                .index(1)
        );
    let deno_command = Command::new("deno")
        .about("Deno version management")
        .subcommand(Command::new("list")
            .about("List installed deno versions")
        )
        .subcommand(Command::new("add")
            .about("Install Deno with version")
            .arg(
                Arg::new("default")
                    .long("default")
                    .takes_value(false)
                    .help("Set as default version")
                    .required(false)
            )
            .arg(Arg::new("version")
                .required(true)
                .help("Deno version")
                .index(1)
            )
        )
        .subcommand(Command::new("default")
            .about("Set default Deno version")
            .arg(Arg::new("version")
                .required(true)
                .help("Default Deno version for DBang")
                .index(1)
            )
        )
        .subcommand(Command::new("delete")
            .about("Delete local installed Deno")
            .arg(Arg::new("version")
                .required(true)
                .help("Deno version")
                .index(1)
            )
        );
    let trust_command = Command::new("trust")
        .about("Trust management for catalogs")
        .subcommand(Command::new("list")
            .about("List trusted catalogs")
        )
        .subcommand(Command::new("add")
            .about("Add new trusted catalog")
            .arg(Arg::new("repo_name")
                .required(true)
                .help("GitHub repo name, e.g. github_user or github_user/repo")
                .index(1)
            )
        )
        .subcommand(Command::new("delete")
            .about("Delete trusted catalog")
            .arg(Arg::new("repo_name")
                .required(true)
                .help("GitHub repo name, e.g. github_user or github_user/repo")
                .index(1)
            )
        );
    let install_command = Command::new("install")
        .about("Install app from catalog")
        .arg(
            Arg::new("name")
                .long("name")
                .takes_value(true)
                .help("Custom app name for script")
                .required(false),
        )
        .arg(
            Arg::new("script")
                .help("script full name")
                .required(true)
                .index(1)
        );
    let uninstall_command = Command::new("uninstall")
        .about("Uninstall app")
        .arg(
            Arg::new("name")
                .help("App name for script")
                .required(true)
                .index(1)
        );
    let apps_command = Command::new("apps")
        .about("List installed apps");
    let catalog_command = Command::new("catalog")
        .about("Catalog management")
        .subcommand(Command::new("list")
            .about("List installed catalogs")
        )
        .subcommand(Command::new("show")
            .about("Display catalog info")
            .arg(Arg::new("repo_name")
                .required(true)
                .help("GitHub repo name, e.g. github_user or github_user/repo")
                .index(1)
            )
        )
        .subcommand(Command::new("add")
            .about("Add new catalog")
            .arg(Arg::new("repo_name")
                .required(true)
                .help("GitHub repo name, e.g. github_user or github_user/repo")
                .index(1)
            )
        )
        .subcommand(Command::new("update")
            .about("Update local catalog")
            .arg(Arg::new("repo_name")
                .required(true)
                .help("GitHub repo name, e.g. github_user or github_user/repo")
                .index(1)
            )
        )
        .subcommand(Command::new("delete")
            .about("Delete local catalog")
            .arg(Arg::new("repo_name")
                .required(true)
                .help("GitHub repo name, e.g. github_user or github_user/repo")
                .index(1)
            )
        );
    let complete_command = Command::new("complete")
        .about("Generate shell completion for zsh & bash")
        .arg(
            Arg::new("zsh")
                .long("zsh")
                .takes_value(false)
                .help("Zsh completion")
                .required(false),
        )
        .arg(
            Arg::new("oh_my_zsh")
                .long("oh_my_zsh")
                .takes_value(false)
                .help("Oh My Zsh")
                .required(false),
        )
        .arg(
            Arg::new("bash")
                .long("bash")
                .takes_value(false)
                .help("Bash completion")
                .required(false),
        );
    // init Clap
    Command::new("dbang")
        .version(VERSION)
        .about("CLI to manage Deno scripts: https://dbang.dev")
        .arg(
            Arg::new("verbose")
                .long("verbose")
                .short('v')
                .takes_value(false)
                .help("Verbose output")
                .required(false),
        )
        .arg(
            Arg::new("quiet")
                .long("quiet")
                .short('q')
                .takes_value(false)
                .help("Suppress diagnostic output")
                .required(false),
        )
        .subcommand(run_command)
        .subcommand(open_command)
        .subcommand(deno_command)
        .subcommand(trust_command)
        .subcommand(install_command)
        .subcommand(uninstall_command)
        .subcommand(apps_command)
        .subcommand(catalog_command)
        .subcommand(complete_command)
        .arg(Arg::new("script")
            .required(false)
            .help("script name")
            .index(1))
        .arg(
            Arg::new("params")
                .required(false)
                .help("params")
                .index(2)
                .multiple_values(true)
        )
}
